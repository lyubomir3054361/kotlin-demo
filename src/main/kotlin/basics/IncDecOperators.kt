package basics

fun main() {
    var number = 10

    // increments the current number first and then prints the value
    println(++number)

    // prints the current number first and then increments the value
    println(number++)
    println(number)

    println()
    
    // decrements the current number first and then prints the value
    println(--number)

    // prints the current number first and then decrements the value
    println(number--)
    println(number)
}
